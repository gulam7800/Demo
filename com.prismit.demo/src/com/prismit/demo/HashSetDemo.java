package com.prismit.demo;

import java.util.HashSet;


public class HashSetDemo {

   public static void main(String args[]) {
      // create a hash set
	     HashSet<String> hs = new HashSet<String>();
      
      // add elements to the hash set
      hs.add("salman");
      hs.add("arman");
      hs.add("lksdlk");
      hs.add("Edmldml");
      hs.add("ddlfldC");
      hs.add("Fdscsdlcl");
      System.out.println(hs);
   }
}